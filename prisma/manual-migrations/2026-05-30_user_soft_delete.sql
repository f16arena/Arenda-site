-- Миграция 2026-05-30: мягкое удаление пользователей (сотрудников/арендаторов)
-- Применять в Supabase SQL Editor. Идемпотентно.
--
-- deleted_at — пометка «удалён из /admin/users». Запись остаётся (FK/аудит/подписи
-- не ломаются), но пользователь скрыт из списка и не возвращается. Деактивация
-- (is_active=false) — отдельно (временно отключить, остаётся в списке).

ALTER TABLE users
  ADD COLUMN IF NOT EXISTS deleted_at TIMESTAMP(3);
