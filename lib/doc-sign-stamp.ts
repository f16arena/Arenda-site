/**
 * Штамп подписанта для блока «Отметка о подписании ЭЦП (НУЦ РК)» на
 * сгенерированных документах (счёт / АВР / акт сверки). Заполняется из реальных
 * DocumentSignature при пересборке подписанной версии (см. lib/signed-generated-doc.ts).
 */
export interface SignStamp {
  /** ФИО/наименование подписанта (из сертификата ЭЦП). */
  name: string
  /** ИИН/БИН подписанта. */
  taxId?: string
  /** Время подписания (локальное, отформатированное). */
  signedAt?: string
  /** Время по метке доверенного времени (TSP, НУЦ РК). */
  tspTime?: string
  /** Текст метода («Документ подписан ЭЦП (НУЦ РК)»). */
  method?: string
}
