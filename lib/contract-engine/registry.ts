// Реестр пунктов договора (спецификация §6). Каждый пункт имеет стабильный
// clauseId; отображаемый номер присваивается при сборке (§3.5, ренумерация).
// Тексты — без жёстких номеров. Портировано из прототипа commrent-constructor.html.

import { type ContractState } from "./schema"
import { UTILITY_GENITIVE } from "./schema"
import { type DerivedContext } from "./derive"
import { money, moneyWithWords, dateLong } from "./numerals"

export interface ClauseChild {
  id: string
  when?: () => boolean
  html: () => string
}

export interface ClauseBlock {
  id: string
  sub?: string // подзаголовок («Арендодатель обязан:»)
  when?: () => boolean
  html: () => string
  children?: ClauseChild[]
}

export interface ClauseSection {
  n: number
  title: string
  when?: () => boolean
  blocks: ClauseBlock[]
}

const PERIOD_LABEL: Record<string, string> = {
  monthly: "ежемесячно",
  quarterly: "ежеквартально",
  annual: "ежегодно",
}

