/**
 * Минимальный склонитель ФИО для русских и казахских фамилий/имён.
 * Используется для конструкций «в лице кого» (родительный падеж) и
 * «действующего на основании» (страдательный причастный оборот тоже
 * требует согласованного склонения).
 *
 * Покрываемые случаи:
 *   - Русские мужские фамилии (-ов/-ев/-ин/-ский/-цкий/-ий) — склоняются;
 *   - Русские женские фамилии (-ова/-ева/-ина/-ская/-цкая) — склоняются;
 *   - Несклоняемые: -ко (Шевченко), -их (Седых), казахские -улы/-ұлы/
 *     -кызы/-қызы/-тегі/-евна/-ызы/-уулу, грузинские -дзе/-швили/-уа,
 *     армянские -ян/-янц, японские/китайские/западные краткие фамилии;
 *   - Имена и отчества: -а/-я → -ы/-и; -ь → -я; согласная → +а;
 *     -ович/-евич → -овича/-евича; «Болат», «Аят», «Бахыт» — мужские казахские
 *     обычно склоняются (по согласной + а).
 *
 * Если фамилия не входит в правила — оставляем как есть (безопасный default).
 */

export type Case = "nominative" | "genitive"

// Хвосты, которые НЕ склоняются (стоп-список). Регистронезависимо.
const INVARIANT_SUFFIXES = [
  // Казахские
  "ұлы", "улы", "қызы", "кызы", "тегі",
  // Грузинские
  "швили", "дзе", "уа",
  // Армянские (-ян склоняем мужские по общим правилам, тут только составные)
  "янц",
  // Украинские
  "ко", "енко",
  // Русские несклоняемые
  "ых", "их",
]

/**
 * Применяет склонение в указанный падеж к одному слову (фамилии, имени или
 * отчеству).  Логика — простая, но покрывает большинство реальных кейсов РК.
 */
function declineWord(word: string, kase: Case): string {
  if (kase === "nominative") return word
  if (!word) return word
  const lower = word.toLowerCase()

  // Несклоняемые суффиксы.
  for (const suf of INVARIANT_SUFFIXES) {
    if (lower.endsWith(suf)) return word
  }

  // Русские/казахские мужские фамилии -ов/-ев/-ёв/-ин → -а
  if (/(?:ов|ев|ёв|ин|ын)$/i.test(word)) return word + "а"

  // Женские фамилии -ова/-ева/-ёва/-ина/-ына → -ой/-ой/-ёй/-ой/-ой
  // (родительный падеж: Ивановой)
  if (/(?:ова|ева|ёва|ина|ына)$/i.test(word)) return word.slice(0, -1) + "ой"

  // Прилагательные мужские: -ский/-цкий/-ий/-ой → -ого
  if (/(?:ский|цкий|ой)$/i.test(word)) return word.slice(0, -2) + "ого"
  if (/(?:ий|ый)$/i.test(word)) return word.slice(0, -2) + "ого"

  // Прилагательные женские: -ская/-цкая/-ая → -ой
  if (/(?:ская|цкая|ая)$/i.test(word)) return word.slice(0, -2) + "ой"

  // Отчества: -ович/-евич/-ьевич → -овича/-евича/-ьевича (м. род)
  if (/(?:ович|евич|ьевич)$/i.test(word)) return word + "а"
  // Отчества женские: -овна/-евна/-ьевна → -овны/-евны/-ьевны
  if (/(?:овна|евна|ьевна)$/i.test(word)) return word.slice(0, -1) + "ы"

  // Имена и обычные слова.
  const last = lower.slice(-1)
  // -а / -я → -ы / -и (Анна → Анны; Илья → Ильи). Касается и имён и фамилий
  // на -а/-я (например «Сапронова», но это уже поймано выше; для имён типа
  // «Айдана» или «Алина»).
  if (last === "а") return word.slice(0, -1) + "ы"
  if (last === "я") return word.slice(0, -1) + "и"
  // -ь → -я (мужские: Игорь → Игоря). Женские на -ь → -и (Любовь → Любови).
  // По умолчанию считаем мужским (для женских надо знать пол).
  if (last === "ь") return word.slice(0, -1) + "я"
  // -й → -я (Алексей → Алексея; Сергей → Сергея)
  if (last === "й") return word.slice(0, -1) + "я"
  // -о / -е — не склоняются (Шевченко, Хитимэ)
  if (last === "о" || last === "е") return word
  // -ы / -и — не склоняются (Седых)
  if (last === "ы" || last === "и") return word
  // -у/-ю — не склоняются (Жаныкулу)
  if (last === "у" || last === "ю") return word
  // Любая согласная — добавляем «а» (Иванов → Иванова — но это поймано выше;
  // для имён: Болат → Болата, Аят → Аята, Иван → Ивана)
  if (/[бвгджзйклмнпрстфхцчшщ]/i.test(last)) return word + "а"

  // Fallback — оставляем как есть.
  return word
}

/**
 * Склоняет ФИО (Фамилия + Имя + Отчество, через пробел) в указанный падеж.
 * Каждое слово склоняется независимо.
 */
export function declineFio(fio: string | null | undefined, kase: Case = "genitive"): string {
  if (!fio) return ""
  if (kase === "nominative") return fio
  const trimmed = fio.trim()
  if (!trimmed) return ""
  return trimmed.split(/\s+/).map((part) => declineWord(part, kase)).join(" ")
}

/**
 * Сокращает «Иванов Иван Иванович» → «Иванов И.И.».
 * Уже умеет: lib/docx-helpers.ts:shortName — дубль здесь чтобы хелпер
 * был самодостаточным.
 */
export function shortenFio(fio: string | null | undefined): string {
  if (!fio) return ""
  const parts = fio.trim().split(/\s+/)
  if (parts.length >= 3) return `${parts[0]} ${parts[1][0]}.${parts[2][0]}.`
  if (parts.length === 2) return `${parts[0]} ${parts[1][0]}.`
  return fio.trim()
}

/**
 * Сокращает ФИО и склоняет в родительный падеж: «Иванов И.И.» (фамилия
 * склоняется, инициалы не меняются).
 */
export function shortenFioGenitive(fio: string | null | undefined): string {
  const short = shortenFio(fio)
  if (!short) return ""
  // Разделим фамилию и инициалы.
  const m = short.match(/^(\S+)(\s+.+)?$/)
  if (!m) return short
  const surname = m[1]
  const initials = m[2] ?? ""
  return declineWord(surname, "genitive") + initials
}
